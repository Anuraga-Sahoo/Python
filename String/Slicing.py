# Slicing in String

b = "hello world"
slicing = b[2 : 5]
print(slicing)

# slice from the start
slicing = b [ : 5]
print(slicing)

#slice from the end
slicing = b [2:]
print(slicing)

# negative indexing
# Use negative indexes to start the slice from the end of the string:

slicing = b[-5: -2]
print(slicing)

